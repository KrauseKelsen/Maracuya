package cruxui.android.maracuya.ui.components.buttons.navigation.group.variant

/**
 * Variantes estructurales permitidas para ButtonNavigationGroup.
 *
 * Define unicamente las combinaciones expuestas por el componente. Los detalles de
 * iconos, labels y variantes de los botones hijos se resuelven en el VariantResolver.
 */
enum class ButtonNavigationGroupVariant {
    BACK_CONTINUE,
    BACK_SUBMIT,
    SKIP_CONTINUE,
    SKIP_SUBMIT,
}
