package cruxui.android.maracuya.ui.components.buttons.navigation.simple.variant

/**
 * Declara las variantes visuales disponibles para ButtonNavigation.
 *
 * PRIMARY hereda el estilo visual de ButtonMrcy primary y ubica el icono a la derecha.
 * SECONDARY hereda el estilo visual de ButtonMrcy secondary y ubica el icono a la izquierda.
 * PRIMARYSQUARE y SECONDARYSQUARE declaran variantes icon-only sin label.
 */
enum class ButtonNavigationVariant {
    PRIMARY,
    SECONDARY,
    PRIMARYSQUARE,
    SECONDARYSQUARE,
}
